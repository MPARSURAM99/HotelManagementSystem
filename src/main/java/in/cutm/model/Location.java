package in.cutm.model;

public class Location {
	String branchName;

	public Location() {
		super();
	}

	public Location(String branchName) {
		super();
		this.branchName = branchName;
	}

	public String getBranchName() {
		return branchName;
	}

	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}
	
	
}